package com.dheeraj.fantasyfootball.model;

public class PlayerPerformanceStats {

	private float ictIndex;

	private float influenceScore;

	private float creativityScore;

	private float threatScore;

	public float getIctIndex() {
		return ictIndex;
	}

	public void setIctIndex(float ictIndex) {
		this.ictIndex = ictIndex;
	}

	public float getInfluenceScore() {
		return influenceScore;
	}

	public void setInfluenceScore(float influenceScore) {
		this.influenceScore = influenceScore;
	}

	public float getCreativityScore() {
		return creativityScore;
	}

	public void setCreativityScore(float creativityScore) {
		this.creativityScore = creativityScore;
	}

	public float getThreatScore() {
		return threatScore;
	}

	public void setThreatScore(float threatScore) {
		this.threatScore = threatScore;
	}
}
